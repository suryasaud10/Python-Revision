# Data types in Python
# Data types are used to classify the type of data that a variable can hold. Python has several built-in data types, including: 
# string, integer, float, boolean, list, tuple, dictionary, and more.

text = 'Hello, World!'
number = "1234"
print(text)
print(type(text))
print(number)
print(type(number))

# integer
int_num = 1234
print(int_num)
print(type(int_num))

# Float
float_num = 12.34
print(float_num)
print(type(float_num))

# Boolean
value = True
print(value)
print(type(value))
