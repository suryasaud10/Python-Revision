#  input()
# input is a built-in function in Python that allows you to take input from the user. 
# When the input() function is called, the program will pause and wait for the user to type something and press Enter. 
# The input provided by the user is then returned as a string.  

input_user = input("Enter your name: \n")
print(input_user)


print(f"Hello, {input_user}!")