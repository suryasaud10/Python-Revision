
per = int(input("Enter your percentage: "))
if per >= 80:
    print("You got A+ grade")
elif per >= 70:
    print("You got A grade")
elif per >= 60:
    print("You got B+ grade")
elif per >= 50:
    print("You got B grade")
elif per >= 40:
    print("You got C grade")
else:
    print("You are failed")